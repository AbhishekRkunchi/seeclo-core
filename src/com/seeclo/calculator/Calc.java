package com.seeclo.calculator;

public class Calc {

    public int add(int a, int b){
        int c = a+b;
        return c;
    }

    public float add(float a, float b){
        float c = a+b;
        return c;
    }

    public long add(long a, long b){
        long c = a+b;
        return c;
    }

    public double add(double a, double b){
        double c = a+b;
        return c;
    }

    public int add(int a, int b, int c){
        int d = a+b+ c;
        return d;
    }

}
